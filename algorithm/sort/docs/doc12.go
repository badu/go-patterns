//
// Stability
// ===========================================
// 1. Bubble Sort: Yes
// 2. Selection Sort: The default implementation is not stable. However it can be made stable.
// 3. Insertion Sort: Yes
// 4. Merge Sort: Yes
// 5. Quick Sort: The default implementation is not stable.
//                However any sorting algorithm can be made stable by considering indexes as comparison parameter.
// 6. Counting Sort: Yes
// 7. Heap Sort: Its typical implementation is not stable, but can be made stable

//
// In Place
// ===========================================
// 1. Bubble Sort: Yes
// 2. Selection Sort: Yes
// 3. Insertion Sort: Yes
// 4. Merge Sort: No. Note that merge sort requires O(n) extra space.
// 5. Quick Sort: As per the broad definition of in-place algorithm it qualifies as an in-place sorting algorithm as it
//                uses extra space only for storing recursive function calls but not for manipulating the input.
// 6. Counting Sort: No
// 7. Heap Sort: Yes
//
// Best Case
// ===========================================
// 1. Bubble Sort: O(n)
// 2. Selection Sort: O(n^2)
// 3. Insertion Sort: O(n)
